#include "BoxDustGrid.hpp"
#include "FatalError.hpp"
#include "Log.hpp"

using namespace std;

//////////////////////////////////////////////////////////////////////

BoxDustGrid::BoxDustGrid()
{
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setupSelfBefore()
{
    DustGrid::setupSelfBefore();

    if (_xmax <= _xmin)
        throw FATALERROR("The extent of the box should be positive in the X direction");
    if (_ymax <= _ymin)
        throw FATALERROR("The extent of the box should be positive in the Y direction");
    if (_zmax <= _zmin)
        throw FATALERROR("The extent of the box should be positive in the Z direction");
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMinX(double value)
{
    _xmin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::minX() const
{
    return _xmin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMaxX(double value)
{
    _xmax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::maxX() const
{
    return _xmax;
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMinY(double value)
{
    _ymin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::minY() const
{
    return _ymin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMaxY(double value)
{
    _ymax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::maxY() const
{
    return _ymax;
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMinZ(double value)
{
    _zmin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::minZ() const
{
    return _zmin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustGrid::setMaxZ(double value)
{
    _zmax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustGrid::maxZ() const
{
    return _zmax;
}

//////////////////////////////////////////////////////////////////////

int BoxDustGrid::dimension() const
{
    return 3;
}

//////////////////////////////////////////////////////////////////////

Box BoxDustGrid::boundingbox() const
{
    // use our inherited Box
    return extent();
}

