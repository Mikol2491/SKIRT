#include "BoxDustDistribution.hpp"
#include "FatalError.hpp"

using namespace std;

//////////////////////////////////////////////////////////////////////

BoxDustDistribution::BoxDustDistribution()
{
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setupSelfBefore()
{
    DustDistribution::setupSelfBefore();

    if (_xmax <= _xmin)
        throw FATALERROR("The extent of the box should be positive in the X direction");
    if (_ymax <= _ymin)
        throw FATALERROR("The extent of the box should be positive in the Y direction");
    if (_zmax <= _zmin)
        throw FATALERROR("The extent of the box should be positive in the Z direction");
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMinX(double value)
{
    _xmin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::minX() const
{
    return _xmin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMaxX(double value)
{
    _xmax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::maxX() const
{
    return _xmax;
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMinY(double value)
{
    _ymin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::minY() const
{
    return _ymin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMaxY(double value)
{
    _ymax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::maxY() const
{
    return _ymax;
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMinZ(double value)
{
    _zmin = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::minZ() const
{
    return _zmin;
}

//////////////////////////////////////////////////////////////////////

void BoxDustDistribution::setMaxZ(double value)
{
    _zmax = value;
}

//////////////////////////////////////////////////////////////////////

double BoxDustDistribution::maxZ() const
{
    return _zmax;
}

