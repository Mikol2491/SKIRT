#include "BoxClipGeometryDecorator.hpp"
#include "FatalError.hpp"

using namespace std;

////////////////////////////////////////////////////////////////////

BoxClipGeometryDecorator::BoxClipGeometryDecorator()
{
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setupSelfBefore()
{
    ClipGeometryDecorator::setupSelfBefore();

    if (xwidth()<=0) throw FATALERROR("The extent of the box should be positive in the X direction");
    if (ywidth()<=0) throw FATALERROR("The extent of the box should be positive in the Y direction");
    if (zwidth()<=0) throw FATALERROR("The extent of the box should be positive in the Z direction");
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMinX(double value)
{
    _xmin = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::minX() const
{
    return _xmin;
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMaxX(double value)
{
    _xmax = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::maxX() const
{
    return _xmax;
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMinY(double value)
{
    _ymin = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::minY() const
{
    return _ymin;
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMaxY(double value)
{
    _ymax = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::maxY() const
{
    return _ymax;
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMinZ(double value)
{
    _zmin = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::minZ() const
{
    return _zmin;
}

////////////////////////////////////////////////////////////////////

void BoxClipGeometryDecorator::setMaxZ(double value)
{
    _zmax = value;
}

////////////////////////////////////////////////////////////////////

double BoxClipGeometryDecorator::maxZ() const
{
    return _zmax;
}

////////////////////////////////////////////////////////////////////

int BoxClipGeometryDecorator::dimension() const
{
    return 3;
}

////////////////////////////////////////////////////////////////////

bool BoxClipGeometryDecorator::inside(Position bfr) const
{
    return contains(bfr);
}

