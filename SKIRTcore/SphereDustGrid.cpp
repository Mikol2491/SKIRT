#include "FatalError.hpp"
#include "SphereDustGrid.hpp"

using namespace std;

//////////////////////////////////////////////////////////////////////

SphereDustGrid::SphereDustGrid()
    : _rmax(0)
{
}

//////////////////////////////////////////////////////////////////////

void SphereDustGrid::setupSelfBefore()
{
    DustGrid::setupSelfBefore();
    if (_rmax <= 0)
        throw FATALERROR("The outer radius of the grid should be positive");
    setBoundingbox(Box(-_rmax,-_rmax,-_rmax,_rmax,_rmax,_rmax));
}

//////////////////////////////////////////////////////////////////////

void SphereDustGrid::setMaxR(double value)
{
    _rmax = value;
}

//////////////////////////////////////////////////////////////////////

double SphereDustGrid::maxR() const
{
    return _rmax;
}

